#include <bits/stdc++.h>

using namespace std;

long long dp[10][101];
int main()
{
    int n;
    cin >> n;
    for (int i = 0; i < 10; i++) {
        dp[i][1] = 1;
    }
    for (int i = 2; i <= n; i++) {
        dp[0][i] = (dp[4][i - 1] + dp[6][i - 1]) % 1000000000;
        dp[1][i] = ( dp[6][i - 1] + dp[8][i - 1]) % 1000000000;
        dp[2][i] = (dp[9][i - 1] + dp[7][i - 1]) % 1000000000;
        dp[3][i] = ( dp[8][i - 1] +  dp[4][i - 1]) % 1000000000;
        dp[4][i] = (dp[3][i - 1] + dp[9][i - 1] + dp[0][i - 1]) % 1000000000;
        dp[5][i] = 0;
        dp[6][i] = (dp[7][i - 1] + dp[1][i - 1]+ dp[0][i - 1]) % 1000000000;
        dp[7][i] = ( dp[2][i - 1] + dp[6][i - 1]) % 1000000000;
        dp[8][i] = (dp[1][i - 1] + dp[3][i - 1]) % 1000000000;
        dp[9][i] = ( dp[2][i - 1] + dp[4][i - 1]) % 1000000000;

    }
    cout << (dp[1][n] + dp[2][n] + dp[3][n] + dp[4][n] + dp[5][n] + dp[6][n] + dp[7][n] + dp[9][n]) % 1000000000;


}
